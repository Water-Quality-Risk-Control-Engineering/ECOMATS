#!/usr/bin/env python3
"""
材料评价任务
基于催化性能、经济可行性、环境友好性、技术可行性和结构合理性五个维度进行评价
"""

from src.tasks.base_task import BaseTask

class EvaluationTask(BaseTask):
    def __init__(self, llm):
        super().__init__(llm)

    def create_task(self, agent, context_task=None):
        description = """
        请根据以下五个维度评估材料方案的性能：
        1. 催化性能（权重50%）
        2. 经济可行性（权重10%）
        3. 环境友好性（权重10%）
        4. 技术可行性（权重10%）
        5. 结构合理性（权重20%）
        
        其中，催化性能是核心标准。
        
        评估步骤：
        1. 分析材料方案在上述五个维度的表现
        2. 重点关注催化性能是否达到标准
        3. 如果催化性能不符合标准，请明确指出问题并建议返回材料设计阶段重新设计
        4. 如果催化性能达标，再综合评估其他维度
        
        评估要求：
        - 请独立进行评估，不要参考其他专家的评分
        - 重点关注材料的科学合理性和实际应用潜力
        - 对于明显不合理的材料设计，请在结构合理性维度给出低分
        - 使用工具验证材料信息的准确性
        
        评估结果输出格式：
        1. 各维度评分（满分10分）：
           - 催化性能: [评分] (核心标准)
           - 经济可行性: [评分]
           - 环境友好性: [评分]
           - 技术可行性: [评分]
           - 结构合理性: [评分]
        
        2. 核心标准评估：
           - 催化性能: [达标/不达标]
        
        3. 综合评价：
           - 如果核心标准不达标: 返回重新设计，并说明原因
           - 如果核心标准达标: 继续进行其他维度评估并给出总体建议
        """
        
        expected_output = """
        提供详细的材料方案评价报告，包括：
        1. 各维度评分和分析
        2. 核心标准评估结果
        3. 明确的决策建议（通过或返回重新设计）
        4. 如果需要重新设计，提供具体的改进建议
        5. 工具验证结果
        """
        
        return super().create_task(agent, description, expected_output, context_task)